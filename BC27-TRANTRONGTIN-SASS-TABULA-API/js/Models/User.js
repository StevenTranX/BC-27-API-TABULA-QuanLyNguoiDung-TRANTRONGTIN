class User {
  constructor(
    id,
    username,
    fullName,
    password,
    email,
    userType,
    language,
    description,
    image
  ) {
    this.id = id;
    this.username = username;
    this.fullName = fullName;
    this.password = password;
    this.email = email;
    this.userType = userType;
    this.language = language;
    this.description = description;
    this.image = image;
  }
}
